select CODPESSOA, sum(ATRASO) AS MINUTOS, sum(FALTA) as FALTA, YEAR(DATA) AS ANO, MONTH(DATA) AS MES
 from AAFHTFUNAM 
inner join PFUNC AS F ON AAFHTFUNAM.CHAPA = F.CHAPA
where year(data) = 2017 AND ((ATRASO)>0 OR (FALTA)>0) AND F.CHAPA = '012291'
group by YEAR(DATA), MONTH(DATA), CODPESSOA;
